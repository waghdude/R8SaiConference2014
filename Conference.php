<p class="news">
<b>Sai Ram!</b> You are cordially invited to attend the Annual Sathya Sai
Baba Conference of Region 8 on Saturday, Aug 30, 2014 and
Sunday, Aug 31, 2014.
</p>
                       

<p class="news" style="text-align:center;"><b>This year's conference theme is "<i>Living in His Omnipresence.</i>"</b>
</p>


<p class="news"><img src="images/dpac.jpg" style="display:block; float:right; padding-left:9px; padding-bottom:9px; margin-right:-9px; border:0;" >
The regional conference will be held this year at the Duarte Performing Arts Center,
1401 Highland Avenue, in Duarte, California 91010. The conference registration fee for
adults (age 19 and over) is $20 for both Saturday and Sunday or $10 for one day only.
For children and youth (18 years of age and under) registration is free of charge.
</p>
                       

<p class="news">We are blessed to have three very special guest speakers this year: 
<b>Bob Bozzani, Sathya Atreyam, and Srikanth Vaidyanathan</b>.
</p>

<p class="news"><b>Bob Bozzani </b> 
graduated from Occidental College in the northeast Los Angeles
community of Eagle Rock. Following his service as an officer in the U.S. Navy, he
joined the family automobile business that was established in 1911. He served as
President of the Monrovia Chamber of Commerce and as President of the Porsche-Audi
Dealers Association of Southern California. He was a member of the Board of Directors
of the YMCA, and he was also a member of the Rotary Club.
</p>

<p class="news">Bob is a long-time Sai Devotee. He first came to know about Swami in 1972, and his
first trip to see Swami was in 1974. He and his wife Barbara traveled to Prasanthi
Nilayam many times to be with Swami. Over the past 40 years he served in many roles
in the Sai Organization. Bob was President of the Sathya Sai Baba Society and Book
Center of America for over 25 years.
</p>
                       

<p class="news"><b>Sathya Atreyam's </b> 
parents journeyed from Shirdi to Parthi in the late 1970s. Sathya's
mother, through her role as the Mumbai Ladies Seva Dal convenor for more than 20
years, is his main inspiration. Sathya's father served as an honorary professor in the
Department of Chemistry in Sathya Sai University after retiring from his career as a
nuclear scientist. Sathya's younger sister is an Anantapur alumnus.
</p>


<p class="news">Sathya has been serving in the Sathya Sai Organization in various capacities in India
and in the USA. After completing his Diploma through a 9-year Bal Vikas program, he
served in different wings of the All India Sri Sathya Sai Organization. Sathya is currently
a member of the USA Sathya Sai Central Council and serves as the President of the
Northeast Region, Region 1, USA.
</p>
                       

<p class="news">Sathya has worked for more than 20 years in the telecommunication industry in various
capacities. He is currently working at IDC, a leading market research firm, as an
Industry Analyst tracking disruptive inventions in the field of communications. He plans
to shortly launch a telecom startup based on the principles of what he calls SAI
Entrepreneurship.

</p>


<p class="news"><b>Srikanth Vaidyanathan </b> studied in Swami's School and University from 1983 to 1998.
He was personally guided by Swami in public speaking, dramatics and music. Srikanth
has spoken in Swami's presence a number of times and continues to speak in many
retreats across the US. He currently serves as the Regional Devotion Coordinator for
the Northeast Region, Region 1, and lives in the Boston Area.
</p>
                       

<p class="news">In addition to these three very special Guest Speakers, the conference program
includes a Young Adult presentation, a Children's presentation, bhajans, Sai sharing by
selected speakers, and a workshop led by Bob Bozanni. Please plan to attend this very
special conference.
</p>


<p class="news">SSE Program: Parents of SSE-age children aged 6 and above are urged to register for
the conference for both Saturday and Sunday. The Sai Spiritual Education program this
year will focus on spiritual, fun-filled sessions in the morning and afternoon on both
Saturday and Sunday. These sessions will lead to a children's presentation to the
general assembly on Sunday afternoon. Your child's attendance in the SSE sessions on
both days will allow him/her to participate in the children's presentation on Sunday
afternoon.
</p>
                       

<p class="news">Parents can enjoy key conference talks while their children are engaged in value-based
activities with other children of their own age. More details will be forwarded to parents
as the date of the conference draws closer.
</p>

<p class="news"><span style="color:rgba(255,51,0,1);"><b>REGISTRATION DEADLINE is Saturday, August 16, 2014.</b></span>
</p>
                       

<p class="news">Please register before the deadline in order to help the organizers plan for the event.
You may register and pay online or by mail. Register and pay online by credit card at
the Region 8 website (http://www.region8saicenters.org/Conference.php.) Register by
mail by obtaining a registration form from your Sai Center or from the Region 8 website,
completing the form, and mailing it along with a check for the registration fee to:
</p>


<p class="news">Bruce Bouché<br>
2225 Los Encinos Road<br>
Ojai, California 93023
</p>
                       

<p class="news">If you have any questions or need additional information, please call or email the
Conference Registrar, Bruce Bouché, at <a href="mailto:bruce@brucebouche.com">bruce@brucebouche.com</a> or 805-807-3309.

</p>
  

<p class="news"><b>DINING INFORMATION:</b> Lunch, tea and snacks will be provided on Saturday and
Sunday at no cost to registrants. In addition, on both Saturday and Sunday at the close
of the day's conference program, boxed prasadam will be provided to all participants.
</p>

